#define NO_DATA 0x80
#define MODE_FIGURES 0x40
#define MODE_LETTERS 0x20
#define MODE_MASK (MODE_FIGURES|MODE_LETTERS)
#define CHARACTER_MASK 0b00011111

inline unsigned char getCode(const int offset) {
  return pgm_read_byte(ustty_map + offset);
}

// Return true if the character exists in the baudot
// character set
bool hasChar(char character) {
  return (character < 128) && 
    (character >= 0) &&
    getCode(character) != NO_DATA;
}

// True for letters mode; false for figures mode
bool getMode(char character) {
  return getCode(character) & MODE_LETTERS;
}

// 5-bit baudot code, MSB first
char getBaudot(char character) {
  return getCode(character) & CHARACTER_MASK;
}

